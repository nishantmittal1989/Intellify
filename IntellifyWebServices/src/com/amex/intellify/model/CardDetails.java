package com.amex.intellify.model;

public class CardDetails {

	private Discover discover;
	private Amex amex;
	private Citi citi;
	private Chase chase;
	private Bofa bofa;
	
	public Discover getDiscover() {
		return discover;
	}
	public void setDiscover(Discover discover) {
		this.discover = discover;
	}
	public Amex getAmex() {
		return amex;
	}
	public void setAmex(Amex amex) {
		this.amex = amex;
	}
	public Citi getCiti() {
		return citi;
	}
	public void setCiti(Citi citi) {
		this.citi = citi;
	}
	public Chase getChase() {
		return chase;
	}
	public void setChase(Chase chase) {
		this.chase = chase;
	}
	public Bofa getBofa() {
		return bofa;
	}
	public void setBofa(Bofa bofa) {
		this.bofa = bofa;
	}
	
	

}
